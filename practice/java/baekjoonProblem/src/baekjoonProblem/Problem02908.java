package baekjoonProblem;

import java.util.*;

// 문제 ( 문자열 단계 )
// 상근이의 동생 상수는 수학을 정말 못한다.
// 상수는 숫자를 읽는데 문제가 있다.
// 이렇게 수학을 못하는 상수를 위해서 상근이는 수의 크기를 비교하는 문제를 내주었다.
// 상근이는 세 자리 수 두 개를 칠판에 써주었다.
// 그 다음에 크기가 큰 수를 말해보라고 했다.
// 상수는 수를 다른 사람과 다르게 거꾸로 읽는다.
// 예를 들어, 734와 893을 칠판에 적었다면, 상수는 이 수를 437과 398로 읽는다.
// 따라서, 상수는 두 수 중 큰 수인 437을 큰 수라고 말할 것이다.
// 두 수가 주어졌을 때, 상수의 대답을 출력하는 프로그램을 작성하시오.

// 입력
// 첫째 줄에 상근이가 칠판에 적은 두 수 A와 B가 주어진다.
// 두 수는 같지 않은 세 자리 수이며, 0이 포함되어 있지 않다.

// 출력
// 첫째 줄에 상수의 대답을 출력한다.

// 예제 입력
// (1)	734 893
// (2)	221 231
// (3)	839 237

// 예제 출력
// (1)	437
// (2)	132
// (3)	938

public class Problem02908 {
	
	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		
		int first = scanner.nextInt();
		int second = scanner.nextInt();
		
		int firstReverse = ((first % 10) * 100) + ((first % 100) - (first % 10)) + (first / 100);
		int secondReverse = ((second % 10) * 100) + ((second % 100) - (second % 10)) + (second / 100);
		
		int compare = 0;
		
		if(firstReverse > secondReverse) compare = firstReverse;
		else compare = secondReverse;
		
		System.out.println(compare);
		
		scanner.close();
	}

}
