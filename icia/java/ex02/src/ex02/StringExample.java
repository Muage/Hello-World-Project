package ex02;

public class StringExample {
	public static void stringExample() {
		
		System.out.println("문자타입(char, string)");
		char ban = 'A';
		String name = "홍길동";
		System.out.println("반:" + ban);
		System.out.println("이름:" + name + "\n");
		
		ban = 'B';
		name = "심청이";
		System.out.println("반:" + ban);
		System.out.println("이름:" + name + "\n");
		
		ban = 'C';
		name = "강감찬";
		System.out.println("반:" + ban);
		System.out.println("이름:" + name);
	}
}
